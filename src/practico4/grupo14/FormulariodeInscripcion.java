    // Cargar materias en el ComboBox
    private void cargarMaterias() {
        DefaultComboBoxModel<Materia> model = new DefaultComboBoxModel<>();
        model.addElement(null);
        if (practico4.listaMaterias.isEmpty()) {
            model.addElement(new Materia(0, "No hay materias cargadas", 0));
        } else {
            for (Materia materia : practico4.listaMaterias) {
                model.addElement(materia);
            }

        }
        comboMaterias.setModel(model);

    }

    private void cargarAlumnos() {
        DefaultComboBoxModel<Alumno> model2 = new DefaultComboBoxModel<>();
        model2.addElement(null);
        if (practico4.listaAlumnos.isEmpty()) {
            model2.addElement(new Alumno(0, "No hay alumnos cargados", ""));
        } else {
            for (Alumno alumno : practico4.listaAlumnos) {
                model2.addElement(alumno);
            }

        }
        comboAlumnos.setModel(model2);
        verMateriasInscriptas();

    }

